# Consultar el tipo de dato
print(type("Hola mundo"))                                       # Tipo 'str'
print(type(5))                                                  # Tipo 'int'
print(type(1.5))                                                # Tipo 'float'
print(type(True))                                               # Tipo 'bool'
print(type(1 + 3j))                                             # Tipo 'complex'
print(type([1, 2, 3, 4]))                                       # Tipo 'list'
print(type({'name':'Asabeneh','age':250, 'is_married':250}))    # Tipo 'dict'
print(type((1,2)))                                              # Tipo 'tuple'
print(type(zip([1,2],[3,4])))                                   # Tipo 'set'